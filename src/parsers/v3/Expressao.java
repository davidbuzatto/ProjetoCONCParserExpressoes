package parsers.v3;

/**
 * Representação de uma expressão.
 * 
 * @author Prof. Dr. David Buzatto
 */
public class Expressao {
    
    private int ranque;
    private int nivel;

    public int getRanque() {
        return ranque;
    }

    public void setRanque( int ranque ) {
        this.ranque = ranque;
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel( int nivel ) {
        this.nivel = nivel;
    }
        
}
